#include "./libft.h"

int		ft_count_char(char *str, char c)
{
	int		j;
	int		i;

	j = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			j++;
		i++;
	}
	return (j);
}
